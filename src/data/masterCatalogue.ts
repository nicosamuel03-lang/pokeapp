/** Recherche dans le catalogue avec diacritiques et casse ignorés */
export const searchCatalogue = (query: string): CatalogueItem[] => {
  if (!query.trim()) return [];
  const normalize = (s: string) =>
    s
      .toLowerCase()
      .normalize("NFD")
      .replace(/[\u0300-\u036f]/g, "");
  const q = normalize(query);
  return masterCatalogue.filter(
    (item) =>
      normalize(item.name).includes(q) ||
      normalize(item.series).includes(q) ||
      normalize(item.type).includes(q)
  );
};
